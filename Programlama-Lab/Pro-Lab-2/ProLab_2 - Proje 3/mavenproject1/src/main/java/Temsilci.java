    private void onaylaHesapButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_onaylaHesapButtonActionPerformed
        // TODO add your handling code here:
        DefaultTableModel model = (DefaultTableModel) hesapTalepTable.getModel();
        String talepDurum = model.getValueAt(hesapTalepTable.getSelectedRow(), 5).toString();

        if (hesapTalepTable.getSelectedRowCount() == 1
                && talepDurum.equals("Onay Bekleniyor")) {
            String talepTur = model.getValueAt(hesapTalepTable.getSelectedRow(), 2).toString();
            String hesapAd = model.getValueAt(hesapTalepTable.getSelectedRow(), 3).toString();
            String musteriID = model.getValueAt(hesapTalepTable.getSelectedRow(), 1).toString();
            String hesapTur = model.getValueAt(hesapTalepTable.getSelectedRow(), 4).toString();
            int hesapTurID = DBislem.getID("Select * from hesap_turu where hesap_tur_adi= '" + hesapTur + "'", this, "hesap_tur_id");
            String talepId = model.getValueAt(hesapTalepTable.getSelectedRow(), 0).toString();
            int hesapID = DBislem.getID("Select * from hesap_talep where hesap_talep_id= " + talepId, this, "hesap_id");
            String sorgu = "UPDATE hesap_talep SET onay_durum= 'Onaylandı' WHERE hesap_talep_id =" + talepId;
            DBislem.updateQuery(sorgu);
            String sorgu2 = "";
            if (!talepTur.equals("Hesap Açma")) {
                sorgu2 = "DELETE FROM hesaplar WHERE hesap_id=" + hesapID;
                DBislem.deleteQuery(sorgu2);
            } else {
                sorgu2 = "INSERT INTO hesaplar(musteri_id, hesap_adi, hesap_tur_id, hesap_bakiye) "
                        + "VALUES (" + musteriID + ", '" + hesapAd + "', " + hesapTurID + ", 0)";
                DBislem.insertQuery(sorgu2);
            }

            TabloIslem.getInstance().showHesapTalepTable(hesapTalepTable, temsilci_id, 2);
        } else if (hesapTalepTable.getSelectedRowCount() > 1) {
            JOptionPane.showMessageDialog(this, "Tek seferde maksimum bir adet talep seçebilirsiniz.");
        } else if (!talepDurum.equals("Onay Bekleniyor")) {
            JOptionPane.showMessageDialog(this, "Bu talep daha önceden işlenmiş!");
        } else {
            JOptionPane.showMessageDialog(this, "Lütfen onaylamak istediğiniz talebi seçiniz!");
        }

    }//GEN-LAST:event_onaylaHesapButtonActionPerformed

