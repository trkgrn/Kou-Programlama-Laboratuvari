    private void krediOnaylaButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_krediOnaylaButtonActionPerformed
        // TODO add your handling code here:
        DefaultTableModel model = (DefaultTableModel) krediTalepTable.getModel();
        String talepDurum = model.getValueAt(krediTalepTable.getSelectedRow(), 4).toString();

        if (krediTalepTable.getSelectedRowCount() == 1
                && talepDurum.equals("Onay Bekleniyor")) {
            String basvuru_id = model.getValueAt(krediTalepTable.getSelectedRow(), 0).toString();
            String musteri_id = model.getValueAt(krediTalepTable.getSelectedRow(), 1).toString();
            String miktar = model.getValueAt(krediTalepTable.getSelectedRow(), 2).toString();
            String toplam_ay = model.getValueAt(krediTalepTable.getSelectedRow(), 3).toString();
            double faiz = DBislem.getFaiz("Kredi Faizi");
            double faizli_miktar = Double.parseDouble(miktar) + Double.parseDouble(miktar) * (faiz / 100);
            double aylik_borc = faizli_miktar / Integer.parseInt(toplam_ay);
            int hesap_id = DBislem.getID("SELECT * from kredi_basvuru where kredi_basvuru_id=" + basvuru_id, this, "hesap_id");
            long fark = DBislem.getTarihFark(Tarih.currentDate);
            String sorgu = "UPDATE kredi_basvuru SET onay_durum= 'Onaylandı' WHERE kredi_basvuru_id =" + basvuru_id;
            DBislem.updateQuery(sorgu);

            String sorgu2 = "INSERT INTO kredi"
                    + "(kredi_basvuru_id, musteri_id, alinan_kredi_toplam, alinan_kredi_toplam_faizli, toplam_ay, kalan_ay) "
                    + "VALUES (" + basvuru_id + ", " + musteri_id + ", " + miktar + ", " + faizli_miktar + ", " + toplam_ay + ", " + toplam_ay + ")";
            DBislem.insertQuery(sorgu2);

            int kredi_id = DBislem.getID("SELECT * from kredi where kredi_basvuru_id=" + basvuru_id, this, "kredi_id");
            String sorgu3 = "";
            for (int i = 0; i < Integer.parseInt(toplam_ay); i++) {
                sorgu3 = "INSERT INTO kredi_odeme(\n"
                        + " kredi_id, aylik_borc, odenen_ay, odendi_mi, gecikme_zammi, kalan_aylik_borc, odeme_aralik)\n"
                        + " VALUES (" + kredi_id + ", " + aylik_borc + ", " + (i + 1) + ", false, 0, " + aylik_borc + ", "
                        + "daterange(CURRENT_DATE + " + (30 * i + 1 + fark) + ", CURRENT_DATE + " + (30 * (i + 1) + 1 + fark) + "))";
                DBislem.insertQuery(sorgu3);
            }

            String sorgu4 = "UPDATE hesaplar SET hesap_bakiye = hesap_bakiye+" + miktar + " WHERE hesap_id=" + hesap_id + " and musteri_id=" + musteri_id;
            DBislem.paraYatir(sorgu4);
            double hesap_bakiye = DBislem.getHesapBakiye(hesap_id);
            String sorgu5 = "INSERT INTO islemler(\n"
                    + "	hedef_hesap_id, islem_tur_id, islem_tarihi, islem_tutari, hedef_bakiye, musteri_id)\n"
                    + "	VALUES (" + hesap_id + ", 4, CURRENT_DATE+ " + fark + "," + miktar + ", " + hesap_bakiye + ", " + musteri_id + ")";
            DBislem.insertQuery(sorgu5);
            TabloIslem.getInstance().showKrediTalepTemsilciTable(krediTalepTable, temsilci_id);
        } else if (hesapTalepTable.getSelectedRowCount() > 1) {
            JOptionPane.showMessageDialog(this, "Tek seferde maksimum bir adet talep seçebilirsiniz.");
        } else if (!talepDurum.equals("Onay Bekleniyor"))
            JOptionPane.showMessageDialog(this, "Bu talep daha önceden işlenmiş!");
        else {
            JOptionPane.showMessageDialog(this, "Lütfen onaylamak istediğiniz talebi seçiniz!");
        }
    }//GEN-LAST:event_krediOnaylaButtonActionPerformed

    private void musteriGuncelleButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_musteriGuncelleButtonActionPerformed
        // TODO add your handling code here:
        DefaultTableModel model = (DefaultTableModel) musteriTable.getModel();

        if (musteriTable.getSelectedRowCount() == 1) {
            // burdan devamke
            String musteri_id = model.getValueAt(musteriTable.getSelectedRow(), 0).toString();
            int kisi_id = DBislem.getID("Select * from musteri where musteri_id=" + musteri_id, this, "kisi_id");
            String ad = DBislem.getAd("Select * from kisi where kisi_id=" + kisi_id, this, "ad");
            String soyad = DBislem.getAd("Select * from kisi where kisi_id=" + kisi_id, this, "soyad");
            String tcno = model.getValueAt(musteriTable.getSelectedRow(), 2).toString();
            String telefon = model.getValueAt(musteriTable.getSelectedRow(), 3).toString();
            String mail = model.getValueAt(musteriTable.getSelectedRow(), 4).toString();
            String adres = model.getValueAt(musteriTable.getSelectedRow(), 5).toString();

            String newAd = JOptionPane.showInputDialog(null, "Yeni Ad giriniz:", ad);
            String newSoyad = JOptionPane.showInputDialog(null, "Yeni Soyad giriniz:", soyad);
            String newTcno = JOptionPane.showInputDialog(null, "Yeni TCK No giriniz:", tcno);
            String newTel = JOptionPane.showInputDialog(null, "Yeni Telefon Numarası giriniz:", telefon);
            String newMail = JOptionPane.showInputDialog(null, "Yeni Mail Aderesi giriniz:", mail);
            String newAdres = JOptionPane.showInputDialog(null, "Yeni Adres giriniz:", adres);

            String sorgu = "UPDATE kisi\n"
                    + "	SET ad='" + newAd + "', soyad='" + newSoyad + "', tc_no='" + newTcno + "',"
                    + " telefon='" + newTel + "', e_posta='" + newMail + "', adres='" + newAdres + "'\n"
                    + "	WHERE kisi_id=" + kisi_id;
            DBislem.updateQuery(sorgu);

            JOptionPane.showMessageDialog(this, "Müşteri başarıyla güncellendi.");
            TabloIslem.getInstance().showMusteriTable(musteriTable, temsilci_id, 1);
        } else {
            JOptionPane.showMessageDialog(this, "Lütfen güncellemek istediğiniz en fazla 1 adet satırı seçin!");
        }


    }//GEN-LAST:event_musteriGuncelleButtonActionPerformed

