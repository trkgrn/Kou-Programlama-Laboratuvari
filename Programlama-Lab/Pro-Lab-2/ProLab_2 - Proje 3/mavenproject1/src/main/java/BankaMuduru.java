        mudurAdresTF.setEditable(false);
        bankaGiderTF.setEditable(false);
        bankaGelirTF.setEditable(false);

        String krediFaizi = "" + DBislem.getFaiz("Kredi Faizi");
        String gecikmeFaizi = "" + DBislem.getFaiz("Gecikme Faizi");
        String maas = "" + DBislem.getTemsilciMaas();
        krediFaiziTF.setText(krediFaizi);
