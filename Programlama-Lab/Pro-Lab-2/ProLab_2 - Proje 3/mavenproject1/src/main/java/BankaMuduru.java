    private void musteriGuncelleButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_musteriGuncelleButtonActionPerformed
        // TODO add your handling code here:
        DefaultTableModel model = (DefaultTableModel) musteriTable.getModel();

        if (musteriTable.getSelectedRowCount() == 1) {
            String musteri_id = model.getValueAt(musteriTable.getSelectedRow(), 0).toString();
            int kisi_id = DBislem.getID("Select * from musteri where musteri_id=" + musteri_id, this, "kisi_id");
            String ad = DBislem.getAd("Select * from kisi where kisi_id=" + kisi_id, this, "ad");
            String soyad = DBislem.getAd("Select * from kisi where kisi_id=" + kisi_id, this, "soyad");
            String tcno = model.getValueAt(musteriTable.getSelectedRow(), 2).toString();
            String telefon = model.getValueAt(musteriTable.getSelectedRow(), 3).toString();
            String mail = model.getValueAt(musteriTable.getSelectedRow(), 4).toString();
            String adres = model.getValueAt(musteriTable.getSelectedRow(), 5).toString();

            String newAd = JOptionPane.showInputDialog(null, "Yeni Ad giriniz:", ad);
            String newSoyad = JOptionPane.showInputDialog(null, "Yeni Soyad giriniz:", soyad);
            String newTcno = JOptionPane.showInputDialog(null, "Yeni TCK No giriniz:", tcno);
            String newTel = JOptionPane.showInputDialog(null, "Yeni Telefon Numarası giriniz:", telefon);
            String newMail = JOptionPane.showInputDialog(null, "Yeni Mail Aderesi giriniz:", mail);
            String newAdres = JOptionPane.showInputDialog(null, "Yeni Adres giriniz:", adres);

            String sorgu = "UPDATE kisi\n"
                    + "	SET ad='" + newAd + "', soyad='" + newSoyad + "', tc_no='" + newTcno + "',"
                    + " telefon='" + newTel + "', e_posta='" + newMail + "', adres='" + newAdres + "'\n"
                    + "	WHERE kisi_id=" + kisi_id;
            DBislem.updateQuery(sorgu);

            JOptionPane.showMessageDialog(this, "Müşteri başarıyla güncellendi.");
            TabloIslem.getInstance().showMusteriTable(musteriTable, 0, 2);
        } else {
            JOptionPane.showMessageDialog(this, "Lütfen güncellemek istediğiniz en fazla 1 adet satırı seçin!");
        }
    }//GEN-LAST:event_musteriGuncelleButtonActionPerformed

