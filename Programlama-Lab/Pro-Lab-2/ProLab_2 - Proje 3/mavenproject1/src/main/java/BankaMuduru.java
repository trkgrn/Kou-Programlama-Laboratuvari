    private void simulationButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_simulationButtonActionPerformed
        // TODO add your handling code here:
        Tarih.getInstance().tarihiIleriAl();
        tarihLabel.setText(Tarih.myFormat.format(Tarih.getAnlikTarih()));

        int musteri_sayisi = DBislem.getMusteriSayisi();
        int[] musteri_id = DBislem.getMusteriler(musteri_sayisi);
        long fark = DBislem.getTarihFark(Tarih.currentDate);

        for (int i = 0; i < musteri_sayisi; i++) {
            int hesap_id = DBislem.getUygunHesapID(musteri_id[i]);

            double aylik_gelir = DBislem.getAylikGelir(musteri_id[i]);
            String sorgu = "UPDATE hesaplar "
                    + "SET hesap_bakiye = hesap_bakiye+" + aylik_gelir
                    + " WHERE hesap_id=" + hesap_id
                    + " and musteri_id=" + musteri_id[i];
            DBislem.paraYatir(sorgu);

            double hesap_bakiye = DBislem.getHesapBakiye(hesap_id);
            String sorgu2 = "INSERT INTO islemler(\n"
                    + "	hedef_hesap_id, islem_tur_id, islem_tarihi, islem_tutari, hedef_bakiye, musteri_id)\n"
                    + "	VALUES (" + hesap_id + ", 7, CURRENT_DATE+" + fark + "," + aylik_gelir + ", " + hesap_bakiye + ", " + musteri_id[i] + ")";
            DBislem.insertQuery(sorgu2);
        }

        double temsilciMaaslari = DBislem.getTemsilciMaas() * DBislem.getTemsilciSayisi();
        String sorgu3 = "INSERT INTO islemler(\n"
                + "	islem_tur_id, islem_tarihi, islem_tutari)\n"
                + "	VALUES (6,CURRENT_DATE+ " + fark + "," + temsilciMaaslari + ")";
        DBislem.insertQuery(sorgu3);

        bankaGiderTF.setText("" + DBislem.getBankaGider());
        bankaGelirTF.setText("" + DBislem.getBankaGelir());


    }//GEN-LAST:event_simulationButtonActionPerformed

